package fr.istic.aco.editor.utils;

public class Buffer {
    private String text;
    private int id;

    public Buffer(int id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
